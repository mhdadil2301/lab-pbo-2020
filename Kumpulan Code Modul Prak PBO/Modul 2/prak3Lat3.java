public class prak3Lat3 {
    public static void main(String args[]) {
    int a = 10;
    int b = 20;
    System.out.println("a == b = " + (a == b) );
    System.out.println("a != b = " + (a != b) );
    System.out.println("a > b = " + (a > b) );
    System.out.println("a < b = " + (a < b) );
    System.out.println("b >= a = " + (b >= a) );
    System.out.println("b <= a = " + (b <= a) );
    }
   }

/*
a == b (Nilai variabel a sama dengan nilai variabel b maka, False)
a != b (Nilai variabel a tidak sama dengan nilai variabel b maka, True)
a > b (Nilai variabel a lebih besar dari nilai variabel b maka, False)
a < b (Nilai variabel a lebih kecil dari nilai variabel b maka, True)
b >= a (Nilai variabel b lebih besar atau sama dengan nilai a maka, True)
b <= a (Nilai variabel b lebih kecil atau sama dengan nilai a maka, False)

*/