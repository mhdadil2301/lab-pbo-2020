public class IfElseExample {
    public static void main(String[] args) {
    
        int number=13;
        if(number%2==0){
            System.out.println("Angka genap");
    
        }else{
            System.out.println("Angka ganjil");
    
        }
    }
} 

/*
terdapat dua kondisi:
1. jika number modulo 2 maka hasilnya 0. Maka program akan diprint angka genap.
2. jika kondisi sebaliknya, maka program yang akan diprint adalah angka ganjil.

*/